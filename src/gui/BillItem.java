    private void loadItem(String invoId) {

        try {
            String q = "SELECT *\n"
                    + "FROM `invoice_item`\n"
                    + "INNER JOIN `invoice` ON `invoice_item`.`invoice_id` =`invoice`.`id`\n"
                    + "INNER JOIN `stock` ON `invoice_item`.`stock_id`=`stock`.`id`\n"
                    + "INNER JOIN `product` ON `stock`.`product_id`=`product`.`id`\n"
                    + "INNER JOIN `category` ON `product`.`category_id`=`category`.`id`\n"
                    + " WHERE `invoice`.`unique_id`='" + invoId + "'";

            ResultSet rs = MySQL.search(q);
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();

                v.add(rs.getString("invoice_item.stock_id"));
                v.add(rs.getString("product.name"));
                v.add(rs.getString("category.name"));
                v.add(rs.getString("mfd"));
                v.add(rs.getString("exd"));
                v.add(rs.getString("invoice_item.qty"));
                v.add(df.format(Double.valueOf(rs.getString("stock.selling_price"))));
                v.add(df.format(Double.valueOf(rs.getString("stock.selling_price")) * Integer.valueOf(rs.getString("invoice_item.qty"))));

                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }


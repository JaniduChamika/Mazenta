    private void makePreview() {
        int r = jTable1.getRowCount();
        String test = "<table width='100%'><tr><th style='text-align:left;'>Test</th><th style='text-align:left;'>Result</th><th style='text-align:left;'>Normal Value</th></tr>";
        for (int i = 0; i < r; i++) {
            String name = "";
            String topic = "";
            String result = "";
            String nValue = "";

            if (jTable1.getValueAt(i, 1).toString().isEmpty()) {
                topic = jTable1.getValueAt(i, 0).toString();
                test += "<tr><td colspan='3'><b>" + topic + "<b></td></tr>";
            } else {
                name = jTable1.getValueAt(i, 0).toString();
                result = jTable1.getValueAt(i, 1).toString();
                nValue = jTable1.getValueAt(i, 2).toString();
                test += "<tr><td>" + name + "</td><td>" + result + "</td><td>" + nValue + "</td></tr>";

            }
        }
        test += "</table>";
        jEditorPane1.setText(test);

    }


    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int r = jTable1.getRowCount();
        String tid = jLabel6.getText();
        String tname = jLabel3.getText();
        boolean isFound = false;
        if (r == 0) {
            JOptionPane.showMessageDialog(this, "Empty report", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {
            try {
                for (int i = 0; i < r; i++) {
                    if (!jTable1.getValueAt(i, 2).toString().isEmpty()) {
                        isFound = true;
                    }
                    if (jTable1.getValueAt(i, 1).toString().isEmpty()) {
                        String topic = jTable1.getValueAt(i, 0).toString();
                        MySQL.iud("INSERT INTO `test_report` (`medical_test_has_tests_id`,`name`) VALUES ('" + tid + "','" + topic + "')");

                    } else {
                        String test = jTable1.getValueAt(i, 0).toString();
                        String result = jTable1.getValueAt(i, 1).toString();
                        String nvalue = jTable1.getValueAt(i, 2).toString();
                        MySQL.iud("INSERT INTO `test_report` (`medical_test_has_tests_id`,`name`,`result`,`nvalue`) VALUES ('" + tid + "','" + test + "','" + result + "','" + nvalue + "')");
                    }

                }
                if (!tid.equals("None")) {

                    MySQL.iud("UPDATE `medical_test_has_tests` SET `status_id`=2 WHERE `medical_test_has_tests`.`id`='" + tid + "'");
                    lab.loadMedicalTest();
                    lab.jtestId.setText("None");
                    lab.jTeasetName.setText("None");
                    lab.jsetbtn.setEnabled(false);

                }
                ResultSet rs = MySQL.search("SELECT * FROM `medical_test_has_tests` INNER JOIN `medical_test` ON `medical_test_has_tests`.`medical_test_id`=`medical_test`.`id` INNER JOIN `reference_no` ON `medical_test`.`reference_no_id`=`reference_no`.`id` INNER JOIN `patient` ON `reference_no`.`patient_id`=`patient`.`id` WHERE `medical_test_has_tests`.`id`='" + tid + "'");
                rs.next();
                String pname = rs.getString("patient.name");
                String contactNo = rs.getString("patient.phone_no");
                String referanceNo = rs.getString("num");
                String pathStream = "src/medicalTest/report.jasper";
           

                Vector v = new Vector();
                for (int i = 0; i < r; i++) {
                    if (!jTable1.getValueAt(i, 2).toString().isEmpty()) {
                        isFound = true;
                    }
                    if (jTable1.getValueAt(i, 1).toString().isEmpty()) {
                        String topic = jTable1.getValueAt(i, 0).toString();
                        v.add(new TestReport(topic, "", "", ""));

                    } else {
                        String test = jTable1.getValueAt(i, 0).toString();
                        String result = jTable1.getValueAt(i, 1).toString();
                        String nvalue = jTable1.getValueAt(i, 2).toString();
                        v.add(new TestReport("", test, result, nvalue));
                    }

                }
                HashMap parameters = new HashMap();
                parameters.put("testname", tname);

                parameters.put("barcode", referanceNo);
                parameters.put("reference", referanceNo);
                parameters.put("todate", datef.format(new Date()));
                parameters.put("pname", pname);
                parameters.put("pphone", contactNo);
                if (isFound) {
                    parameters.put("column3", "Normal Value");
                } else {
                    parameters.put("column3", "");
                }
                JRBeanCollectionDataSource dataSource = new JRBeanCollectionDataSource(v);
                JasperPrint jp = JasperFillManager.fillReport(pathStream, parameters, dataSource);
                JasperViewer.viewReport(jp, false);
                this.dispose();

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton5ActionPerformed


    private void testListner() {
        ListSelectionListener lsl = new ListSelectionListener() {
            @Override
            public void valueChanged(ListSelectionEvent e) {
                int r = jTable1.getSelectedRow();
                if (r != -1) {
                    jTextField1.setText(jTable1.getValueAt(r, 1).toString());
                    try {
                        jTextField4.setText(df2.format(df.parse(jTable1.getValueAt(r, 2).toString())));
                    } catch (ParseException ex) {
                        ex.printStackTrace();
                    }
                    jButton1.setEnabled(false);
                    jButton4.setEnabled(true);
                }
            }
        };
        jTable1.getSelectionModel().addListSelectionListener(lsl);
    }

    private void loadTest() {
        try {
            ResultSet rs = MySQL.search("SELECT * FROM `tests` ORDER BY `id` ASC");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                v.add(df.format(Double.parseDouble(rs.getString("fee"))));
                dtm.addRow(v);
            }
            DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
            centerRenderer.setHorizontalAlignment(JLabel.CENTER);
            for (int i = 0; i < 3; i++) {
                jTable1.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }


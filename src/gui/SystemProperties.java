    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String test = jTextField1.getText();
        String fee = jTextField4.getText();
        if (test.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter test name", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (fee.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter test fee", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (!Pattern.compile("[1-9]+[0-9]*|[1-9]+[0-9]*[.][0-9]{0,2}|0[.][0-9][1-9]").matcher(fee).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid fee format", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet rs1 = MySQL.search("SELECT * FROM `tests` WHERE `name`='" + test + "'");
                if (rs1.next()) {
                    JOptionPane.showMessageDialog(this, test + " is already exist", "Warning", JOptionPane.WARNING_MESSAGE);

                } else {
                    MySQL.iud("INSERT INTO `tests` (`name`,`fee`) VALUES ('" + test + "','" + fee + "')");

                }
                loadTest();
                resetTest();
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        String test = jTextField1.getText();
        String fee = jTextField4.getText();
        int r = jTable1.getSelectedRow();
        String id = jTable1.getValueAt(r, 0).toString();
        if (test.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter test name", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (fee.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter test fee", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (!Pattern.compile("[1-9]+[0-9]*|[1-9]+[0-9]*[.][0-9]{0,2}|0[.][0-9][1-9]").matcher(fee).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid fee format", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            MySQL.iud("UPDATE `tests` SET `name`='" + test + "',`fee`='" + fee + "' WHERE `id`='" + id + "'");
            loadTest();
            resetTest();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jTextField4KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField4KeyTyped
        String price = jTextField4.getText();

        if (!Pattern.compile("0|0[.]|0[.][0-9]{0,1}|0[.][0-9]{0,1}[1-9]{0,1}|[1-9]+|[1-9]+[0-9]+|[1-9]+[.]|[1-9]+[0-9]*[.][0-9]{0,2}").matcher(price + evt.getKeyChar()).matches()) {
            evt.consume();
        }
    }//GEN-LAST:event_jTextField4KeyTyped


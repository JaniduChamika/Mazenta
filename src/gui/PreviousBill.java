    private void loadBill() {

        String grnid = jSearch1.getText();
        Date date = jDateChooser1.getDate();
        try {
            String q = "SELECT *\n"
                    + "FROM `invoice`\n"
                    + "INNER JOIN `invoice_payment` ON `invoice`.`id`=`invoice_payment`.`invoice_id`\n"
                    + "INNER JOIN `payment_method` ON `invoice_payment`.`payment_method_id`=`payment_method`.`id`";

            if (!grnid.isEmpty() || date != null) {
                q += " WHERE ";
            }
            Vector queryVector = new Vector();

            if (!grnid.isEmpty()) {
                queryVector.add("`invoice`.`unique_id`='" + grnid + "'");
            }
            if (date != null) {
                queryVector.add("`invoice`.`date_time`='" + datef.format(date) + "'");
            }
            for (int i = 0; i < queryVector.size(); i++) {
                q += queryVector.get(i);
                if (i < queryVector.size() - 1) {
                    q += " AND ";

                }
            }
            ResultSet rs = MySQL.search(q);
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();

                v.add(rs.getString("invoice.unique_id"));
                String q2 = "SELECT COUNT(`invoice_id`)  AS `item` FROM `invoice_item`"
                        + " WHERE `invoice_item`.`invoice_id`='" + rs.getString("invoice.id") + "'";
                ResultSet rs2 = MySQL.search(q2);
                rs2.next();
                v.add(rs2.getString("item"));
                v.add(rs.getString("date_time"));

                v.add("Rs. " + df.format(Double.valueOf(rs.getString("payment")) - Double.valueOf(rs.getString("balance"))));
                v.add(rs.getString("payment_method.name"));
                v.add("Rs. " + df.format(Double.valueOf(rs.getString("payment"))));
                v.add("Rs. " + df.format(Double.valueOf(rs.getString("balance"))));

                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }


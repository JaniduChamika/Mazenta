    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
//            InputStream pathStream = PreviousBill.class.getResourceAsStream("../reports/PreviousBill.jasper");
            String pathStream = "src/reports/PreviousBill.jasper";

            HashMap parameters = new HashMap();

            Vector<model.PreviousBILL> v = new Vector();
            for (int i = 0; i < jTable1.getRowCount(); i++) {
                String id = jTable1.getValueAt(i, 0).toString();
                String item = jTable1.getValueAt(i, 1).toString();
                String date = jTable1.getValueAt(i, 2).toString();

                String total = jTable1.getValueAt(i, 3).toString();
                String method = jTable1.getValueAt(i, 4).toString();
                String paid = jTable1.getValueAt(i, 5).toString();
                String balance = jTable1.getValueAt(i, 6).toString();
                v.add(new PreviousBILL(id, item, date, total, method, paid, balance));
            }
            JRBeanCollectionDataSource dataSource = new JRBeanCollectionDataSource(v);
            JasperPrint jp = JasperFillManager.fillReport(pathStream, parameters, dataSource);
            JasperViewer.viewReport(jp, false);
        } catch (Exception e) {
            e.printStackTrace();
        }
        this.dispose();

    }//GEN-LAST:event_jButton2ActionPerformed

